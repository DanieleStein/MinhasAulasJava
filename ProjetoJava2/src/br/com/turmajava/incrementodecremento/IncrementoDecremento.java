package br.com.turmajava.incrementodecremento;

public class IncrementoDecremento {

	public static void main(String[] args) {
		
		
		int a = 1;
		
		//Pós-incremento --> variavel++ = (variavel + 1) Pega o que ta na var e depois incrementa
		
		//System.out.println(a++);
		//System.out.println(a);
		
		
		//Pré-incremento --> ++variavel = (1 + variavel) Incrementa e pega o que ta na var
		
		//System.out.println(++a);
		//System.out.println(a);
		
		
		//Pós-decremento --> variavel-- = (variavel -1) Pega o que ta na var e depois decrementa
		
		//System.out.println(a--);
		//System.out.println(a);
		
		//Pré-decremento --> --variavel = (1 - variavel) decrementa e roda
		
		System.out.println(--a);
		System.out.println(a);
				

	}

}
