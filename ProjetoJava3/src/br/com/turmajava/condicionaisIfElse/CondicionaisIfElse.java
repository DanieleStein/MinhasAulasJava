package br.com.turmajava.condicionaisIfElse;

public class CondicionaisIfElse {
	
	public static void main(String[] args) {
		
		int num1 = 100;
		int num2 = 80;
		
		if (num1 < num2) {
			System.out.println("menor");
		} else if (num1 > num2) {
			System.out.println("maior");
		} else {
			System.out.println("iguais");
		}
		
	
		
	}

}
